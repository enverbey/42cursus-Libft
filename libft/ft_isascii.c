#include "libft.h"

int	ft_isascii(int c)
{
	if ((c < 0) || (c > 127))
		return (0);
	return (1);
}
/*
	Girilen değer ascii tablosundan ise 1 ; değil ise 0 çıktısını verir.	
*/
